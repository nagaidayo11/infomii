function buildPost(p) {
  const tableHeader = p.table[0];
  const tableRows = p.table.slice(1);
  const tableMd = [
    `| ${tableHeader.join(" | ")} |`,
    `| ${tableHeader.map(() => "---").join(" | ")} |`,
    ...tableRows.map((r) => `| ${r.join(" | ")} |`),
  ].join("\n");

  const allExtras = [...p.extras, ...padSections(p)];

  return `---
title: ${p.title}
date: ${p.date}
description: ${p.description}
---

${p.lead}

## ① 共感

${p.empathy}

## ② 問題提起

${p.problem}

## ③ 解決策比較（比較表）

${tableMd}

## ④ ベストな方法

${p.best.map((b) => `- ${b}`).join("\n")}

${p.detail}

${allExtras.join("\n\n")}

現場で試すときは、完璧な整備より「スタッフ全員が同じURLを開ける」ことを先に確認してください。そこが揃えば、本稿の施策は小さく始めても前に進みやすくなります。週次レビューでは、本テーマに関係する問い合わせが1件でも減ったかをメモするだけで十分です。減らなくても、説明時間が短くなった体感があれば前進とみなしてよい場合が多いです。

## ⑤ CTA

まずは小さく試し、現場で続く形に合わせて広げていくのが安全です。

[無料で館内案内を作る](/lp/business)

## 内部リンク候補

- [${p.links[0].title}](/blog/${p.links[0].slug})
- [${p.links[1].title}](/blog/${p.links[1].slug})
`;
}

async function main() {
  await fs.mkdir(OUT, { recursive: true });
  for (const p of POSTS) {
    const body = buildPost(p);
    const chars = [...body.replace(/^---[\s\S]*?---\n?/, "")].length;
    if (chars < 1800) {
      throw new Error(`${p.slug}: body too short (${chars})`);
    }
    await fs.writeFile(path.join(OUT, `${p.slug}.md`), body, "utf8");
    console.log(`ok ${p.date} ${p.slug} (${chars} chars)`);
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
