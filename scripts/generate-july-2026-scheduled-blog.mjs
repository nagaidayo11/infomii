function buildTable(table) {
  const [header, ...rows] = table;
  return [
    `| ${header.join(" | ")} |`,
    `| ${header.map(() => "---").join(" | ")} |`,
    ...rows.map((r) => `| ${r.join(" | ")} |`),
  ].join("\n");
}

function buildPost(p) {
  const sections = [];
  sections.push(`**結論：** ${p.conclusion}\n`);
  sections.push(p.empathy ? `## なぜ今これが問題になるのか\n\n${p.empathy}` : "");
  sections.push(`## 何が起きているか\n\n${p.problem}`);

  if (p.format === "faq" && p.faqs) {
    sections.push(`## よくある質問\n\n${p.faqs.map((f) => `### ${f.q}\n\n${f.a}`).join("\n\n")}`);
    if (p.best) {
      sections.push(`## まとめ：今日からやること\n\n${p.best.map((b) => `- ${b}`).join("\n")}`);
    }
  }

  if (p.format === "template" && p.blocks) {
    sections.push(
      `## コピペ用ブロック一覧\n\n${p.blocks.map((b) => `### ${b[0]}\n\n- 載せる内容：${b[1]}\n- 例：${b[2]}`).join("\n\n")}`,
    );
    if (p.best) {
      sections.push(`## 運用のコツ\n\n${p.best.map((b) => `- ${b}`).join("\n")}`);
    }
  }

  if (p.format === "steps" && p.best) {
    sections.push(`## 手順\n\n${p.best.map((b, i) => `${i + 1}. ${b.replace(/^Step\d+:\s*/, "")}`).join("\n")}`);
  }

  if (p.format === "checklist" && p.checklist) {
    sections.push(`## チェックリスト\n\n${p.checklist.map((c) => `- [ ] ${c}`).join("\n")}`);
    if (p.best) {
      sections.push(`## 運用メモ\n\n${p.best.map((b) => `- ${b}`).join("\n")}`);
    }
  }

  if (p.format === "conclusion" && p.best) {
    sections.push(`## 今日からやること\n\n${p.best.map((b) => `- ${b}`).join("\n")}`);
  }

  if (p.format === "compare" && p.best) {
    sections.push(`## 選び方の目安\n\n${p.best.map((b) => `- ${b}`).join("\n")}`);
  }

  sections.push(`## 比較・整理表\n\n${buildTable(p.table)}`);
  sections.push(`## 補足\n\n${p.detail}`);
  if (p.extras?.length) sections.push(p.extras.join("\n\n"));
  sections.push(padSections(p).join("\n\n"));
  sections.push(
    `### ホテル向け・運用の共通メモ\n\n` +
      `館内案内の改善は、大規模リニューアルより「よく聞かれる質問の上位3つを先頭に移す」ほうが、現場の負担が小さく効果が出やすい傾向があります。` +
      `フロント・清掃・飲食など部門が分かれているホテルでは、ゲスト向けの表示とスタッフ向けの正本を混ぜないことが、更新漏れと誤案内の両方を防ぐ近道です。` +
      `多言語が必要な施設でも、最初から全ページ翻訳する必要はなく、チェックイン・Wi-Fi・大浴場・チェックアウトの4カテゴリだけ英語を厚くする運用でも、インバウンドの自己解決率は上がりやすいです。` +
      `また、案内ページの最下部に「最終更新日」と「お問い合わせ先」を固定しておくと、ゲストが情報の鮮度を判断しやすくなり、電話での確認も減りやすいです。`,
  );
  sections.push(
    `現場では完璧な整備より、スタッフ全員が同じ案内URLを開ける状態を先に作ると、本稿の施策は小さく始めても前に進みやすくなります。導入初期は「全ページ完成」より「よく聞かれる3項目だけ正確」を優先すると、ゲストの信頼も得やすくなります。`,
  );

  const body = `${p.lead || p.conclusion}\n\n${sections.filter(Boolean).join("\n\n")}\n\n## CTA\n\nまずは無料で試し、自施設に合わせて広げていくのが安全です。\n\n[無料で館内案内を作る](/lp/business)\n\n## 内部リンク候補\n\n- [${p.links[0].title}](/blog/${p.links[0].slug})\n- [${p.links[1].title}](/blog/${p.links[1].slug})\n`;

  return `---
title: ${p.title}
date: ${p.date}
description: ${p.description}
---

${body}`;
}

async function main() {
  await fs.mkdir(OUT, { recursive: true });
  for (const p of ALL_POSTS) {
    const content = buildPost(p);
    const chars = [...content.replace(/^---[\s\S]*?---\n?/, "")].length;
    if (chars < 1800) {
      throw new Error(`${p.slug}: body too short (${chars})`);
    }
    await fs.writeFile(path.join(OUT, `${p.slug}.md`), content, "utf8");
    console.log(`ok ${p.date} ${p.slug} (${chars} chars)`);
  }
  console.log(`total: ${ALL_POSTS.length}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
